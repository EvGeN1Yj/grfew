#!/bin/bash
# Скрипт для тестирования балансировки нагрузки

echo "Тестирование балансировки нагрузки..."
echo "Делаем 10 запросов к приложению..."
echo ""

for i in {1..10}; do
  echo -n "Запрос $i: "
  curl -s http://localhost:8080/ | grep -o '"hostname":"[^"]*"' || echo "Ошибка подключения"
  sleep 0.5
done

echo ""
echo "Если вы видите разные hostname'ы, балансировка работает корректно!"

